    def _gen_points_select(self, current: str, op: ast.PointsSelect) -> str:
        spec = op.spec
        if isinstance(spec, ast.Polar):
            n = self._gen_expr(spec.count)
            r = self._gen_expr(spec.radius)
            var = self._new_var()
            self._emit(f'{var}_pts = [({r} * math.cos(2 * math.pi * i / {n}), {r} * math.sin(2 * math.pi * i / {n})) for i in range({n})]')
            return f'{current}.workplane().pushPoints({var}_pts)'
        elif isinstance(spec, ast.Grid):
            nx = self._gen_expr(spec.nx)
            ny = self._gen_expr(spec.ny)
            sp = self._gen_expr(spec.spacing)
            return f'{current}.workplane().rarray({sp}, {sp}, {nx}, {ny})'
        elif isinstance(spec, ast.ListLit):
            pts = self._gen_expr(spec)
            return f'{current}.workplane().pushPoints({pts})'
        else:
            pts = self._gen_expr(spec)
            return f'{current}.workplane().pushPoints({pts})'

    # Rotation angles (degrees) to transform from default XY workplane to target plane
