"""OCP (OpenCascade) Python code generator from PolyScript AST."""

from __future__ import annotations

import enum
from . import ast_nodes as ast
from .colors import resolve_color
from .errors import CodegenError


class PipelineContext(enum.Enum):
    """Context type flowing through a pipeline."""
    WORKPLANE = "Workplane"
    TWO_D = "2D"
    THREE_D = "3D"
    FACE_SELECTION = "FaceSelection"
    EDGE_SELECTION = "EdgeSelection"
    VERTEX_SELECTION = "VertexSelection"
    POINT_SELECTION = "PointSelection"
    UNKNOWN = "unknown"


# 2D primitive names for error messages
_2D_PRIMITIVE_NAMES = {
    ast.Circle: "circle",
    ast.Rect: "rect",
    ast.Ellipse: "ellipse",
    ast.Polyline: "polyline",
    ast.Polygon: "polygon",
    ast.Text: "text",
    ast.SketchExpr: "sketch",
}


_MATH_FUNCS = {
    "sin", "cos", "tan", "asin", "acos", "atan", "atan2",
    "sqrt", "radians", "degrees", "floor", "ceil",
}
_MATH_ALIASES = {"rad": "radians", "deg": "degrees"}

# Selector name aliases -> shorthand notation
_SELECTOR_NAME_ALIASES = {
    "top": ">Z", "bottom": "<Z",
    "right": ">X", "left": "<X",
    "front": ">Y", "back": "<Y",
}

# Selector symbol mapping: PolyScript symbol -> CadQuery selector symbol
_SELECTOR_SYMBOL_MAP = {
    ">": ">",   # max position
    "<": "<",   # min position
    "=": "|",   # parallel
    "+": "#",   # perpendicular
}


def _selector_to_cadquery(sel: str) -> str:
    """Convert a PolyScript selector to a CadQuery selector string.

    Examples:
        ">Z"   -> ">Z"
        "=Z"   -> "|Z"
        "+Z"   -> "#Z"
        "top"  -> ">Z"
        "front" -> ">Y"
    """
    # Resolve name aliases first
    if sel in _SELECTOR_NAME_ALIASES:
        sel = _SELECTOR_NAME_ALIASES[sel]
    # Map symbol if needed
    if len(sel) >= 2 and sel[0] in _SELECTOR_SYMBOL_MAP:
        return _SELECTOR_SYMBOL_MAP[sel[0]] + sel[1:]
    return sel


class OCPCodegen:
    """Generate OCP Python code from a PolyScript AST."""

    _HEADER = [
        "# Generated by PolyScript (OCP backend)",
        "from polyscript import ocp_kernel as cq",
        "import math",
        "",
    ]

    def __init__(self):
        self.lines: list[str] = []
        self.indent = 0
        self._var_counter = 0
        self._result_var = "_result"
        self._functions: dict[str, ast.FuncDef] = {}

    def generate(self, program: ast.Program) -> str:
        self.lines = list(self._HEADER)

        # First pass: collect function definitions
        for stmt in program.statements:
            if isinstance(stmt, ast.FuncDef):
                self._functions[stmt.name] = stmt

        # Generate code
        shape_vars: list[str] = []
        for stmt in program.statements:
            if isinstance(stmt, ast.FuncDef):
                self._gen_funcdef(stmt)
            elif isinstance(stmt, ast.Assignment):
                self._gen_assignment(stmt)
            else:
                var = self._new_var()
                expr_code = self._gen_expr(stmt)
                self._emit(f"{var} = {expr_code}")
                shape_vars.append(var)

        if len(shape_vars) > 1:
            self._emit("")
            self._emit(f"_result = {shape_vars[0]}")
            for v in shape_vars[1:]:
                self._emit(f"_result = _result.union({v})")
        elif len(shape_vars) == 1:
            self._emit("")
            self._emit(f"_result = {shape_vars[0]}")
        else:
            self._emit("_result = None")

        return "\n".join(self.lines)

    def _emit(self, line: str):
        self.lines.append("    " * self.indent + line)

    def _new_var(self) -> str:
        self._var_counter += 1
        return f"_v{self._var_counter}"

    # --- Top Level ---

    def _gen_funcdef(self, node: ast.FuncDef):
        params = ", ".join(node.params)
        self._emit(f"def {node.name}({params}):")
        self.indent += 1
        body_code = self._gen_expr(node.body)
        self._emit(f"return {body_code}")
        self.indent -= 1
        self._emit("")

    def _gen_assignment(self, node: ast.Assignment) -> str:
        expr_code = self._gen_expr(node.value)
        self._emit(f"{node.name} = {expr_code}")
        return node.name

    # --- Expression Dispatch ---

    def _expr_number_lit(self, node: ast.NumberLit) -> str:
        return repr(node.value)

    def _expr_string_lit(self, node: ast.StringLit) -> str:
        return repr(node.value)

    def _expr_var_ref(self, node: ast.VarRef) -> str:
        return node.name

    def _expr_tag_ref(self, node: ast.TagRef) -> str:
        return repr(node.name)

    def _expr_bin_op(self, node: ast.BinOp) -> str:
        return f"({self._gen_expr(node.left)} {node.op} {self._gen_expr(node.right)})"

    def _expr_unary_neg(self, node: ast.UnaryNeg) -> str:
        return f"(-{self._gen_expr(node.operand)})"

    def _expr_tuple_lit(self, node: ast.TupleLit) -> str:
        vals = ", ".join(self._gen_expr(v) for v in node.values)
        return f"({vals})"

    def _expr_list_lit(self, node: ast.ListLit) -> str:
        vals = ", ".join(self._gen_expr(v) for v in node.values)
        return f"[{vals}]"

    def _expr_if_expr(self, node: ast.IfExpr) -> str:
        c = self._gen_expr(node.cond)
        t = self._gen_expr(node.then_expr)
        e = self._gen_expr(node.else_expr)
        return f"({t} if {c} else {e})"

    def _expr_bool_const(self, node: ast.BoolConst) -> str:
        return "True" if node.value else "False"

    def _expr_selector_lit(self, node: ast.SelectorLit) -> str:
        return repr(_selector_to_cadquery(node.value))

    _EXPR_DISPATCH: dict[type, str] = {
        ast.Pipeline:      "_gen_pipeline",
        ast.NumberLit:     "_expr_number_lit",
        ast.StringLit:     "_expr_string_lit",
        ast.VarRef:        "_expr_var_ref",
        ast.TagRef:        "_expr_tag_ref",
        ast.BinOp:         "_expr_bin_op",
        ast.UnaryNeg:      "_expr_unary_neg",
        ast.TupleLit:      "_expr_tuple_lit",
        ast.ListLit:       "_expr_list_lit",
        ast.ListComp:      "_gen_list_comp",
        ast.IfExpr:        "_expr_if_expr",
        ast.BoolConst:     "_expr_bool_const",
        ast.SelectorLit:   "_expr_selector_lit",
        ast.Box:           "_gen_box",
        ast.Cylinder:      "_gen_cylinder",
        ast.Sphere:        "_gen_sphere",
        ast.Cone:          "_gen_cone",
        ast.Torus:         "_gen_torus",
        ast.Wedge:         "_gen_wedge",
        ast.Rect:          "_gen_rect",
        ast.Circle:        "_gen_circle",
        ast.Ellipse:       "_gen_ellipse",
        ast.Polyline:      "_gen_polyline",
        ast.Polygon:       "_gen_polygon",
        ast.Text:          "_gen_text",
        ast.Union:         "_gen_union_source",
        ast.Diff:          "_gen_diff_source",
        ast.Inter:         "_gen_inter_source",
        ast.FuncCall:      "_gen_func_call",
        ast.LinePath:      "_gen_line_path",
        ast.ArcPath:       "_gen_arc_path",
        ast.HelixPath:     "_gen_helix_path",
        ast.BezierPath:    "_gen_bezier_path",
        ast.SplinePath:    "_gen_spline_path",
        ast.SketchExpr:    "_gen_sketch",
    }

    def _gen_expr(self, node: ast.Node) -> str:
        if node is None:
            return "None"
        handler_name = self._EXPR_DISPATCH.get(type(node))
        if handler_name is None:
            raise CodegenError(f"Unknown expression node: {type(node).__name__}")
        return getattr(self, handler_name)(node)

    # --- Pipeline Context Tracking ---

    @staticmethod
    def _source_context(source: ast.Node) -> PipelineContext:
        """Determine the pipeline context from the source node."""
        if isinstance(source, (ast.Box, ast.Cylinder, ast.Sphere, ast.Cone, ast.Torus)):
            return PipelineContext.THREE_D
        if isinstance(source, (ast.Rect, ast.Circle, ast.Ellipse,
                               ast.Polyline, ast.Polygon, ast.Text,
                               ast.SketchExpr)):
            return PipelineContext.TWO_D
        if isinstance(source, (ast.LinePath, ast.ArcPath, ast.BezierPath,
                               ast.HelixPath, ast.SplinePath)):
            return PipelineContext.TWO_D
        if isinstance(source, (ast.Union, ast.Diff, ast.Inter)):
            return PipelineContext.THREE_D
        # VarRef, FuncCall, etc. -- cannot statically determine
        return PipelineContext.UNKNOWN

    @staticmethod
    def _op_output_context(op: ast.Node, current_ctx: PipelineContext) -> PipelineContext:
        """Determine the output context after applying a pipe operation."""
        if isinstance(op, ast.FacesSelect):
            return PipelineContext.FACE_SELECTION
        if isinstance(op, ast.EdgesSelect):
            return PipelineContext.EDGE_SELECTION
        if isinstance(op, ast.VerticesSelect):
            return PipelineContext.VERTEX_SELECTION
        if isinstance(op, ast.PointsSelect):
            return PipelineContext.POINT_SELECTION
        if isinstance(op, ast.Workplane):
            return PipelineContext.WORKPLANE
        if isinstance(op, ast.Place):
            return PipelineContext.TWO_D
        if isinstance(op, ast.Implicit2DPrimitive):
            return PipelineContext.TWO_D
        if isinstance(op, ast.Implicit3DPrimitive):
            return PipelineContext.THREE_D
        if isinstance(op, (ast.Extrude, ast.Revolve, ast.Sweep, ast.Loft, ast.Cut)):
            return PipelineContext.THREE_D
        if isinstance(op, ast.Hole):
            return PipelineContext.THREE_D
        if isinstance(op, (ast.Fillet, ast.Chamfer)):
            # fillet/chamfer from selection contexts -> 3D; from 2D -> 2D
            if current_ctx == PipelineContext.TWO_D:
                return PipelineContext.TWO_D
            return PipelineContext.THREE_D
        if isinstance(op, ast.Shell):
            return PipelineContext.THREE_D
        if isinstance(op, ast.Offset):
            return PipelineContext.TWO_D
        if isinstance(op, (ast.Diff, ast.Union, ast.Inter)):
            return PipelineContext.THREE_D
        if isinstance(op, (ast.Grid, ast.Polar)):
            if current_ctx in (PipelineContext.FACE_SELECTION, PipelineContext.POINT_SELECTION):
                return PipelineContext.POINT_SELECTION
            return current_ctx
        if isinstance(op, (ast.Translate, ast.Rotate, ast.Scale, ast.Mirror)):
            return current_ctx  # preserves context
        if isinstance(op, ast.ColorOp):
            return current_ctx  # preserves context
        if isinstance(op, (ast.Move, ast.MoveTo)):
            return current_ctx  # 2D context operations
        if isinstance(op, ast.AsTag):
            return current_ctx  # preserves context
        return PipelineContext.UNKNOWN

    @staticmethod
    def _validate_2d_primitive_context(
        op: ast.Implicit2DPrimitive, ctx: PipelineContext
    ) -> None:
        """Raise CodegenError if a 2D primitive appears in an invalid context."""
        # Contexts that allow 2D primitives:
        # Workplane, FaceSelection (implicit workplane), VertexSelection, PointSelection, 2D, unknown
        if ctx in (
            PipelineContext.THREE_D,
            PipelineContext.EDGE_SELECTION,
        ):
            prim_name = _2D_PRIMITIVE_NAMES.get(
                type(op.primitive), type(op.primitive).__name__.lower()
            )
            raise CodegenError(
                f"2D primitive '{prim_name}' requires face selection. "
                f"Use '| faces top |' or similar before '| {prim_name} ...'"
            )

    # 3D primitive names for error messages
    _3D_PRIMITIVE_NAMES = {
        ast.Box: "box",
        ast.Cylinder: "cylinder",
        ast.Sphere: "sphere",
        ast.Cone: "cone",
        ast.Torus: "torus",
    }

    @staticmethod
    def _validate_3d_primitive_context(
        op: ast.Implicit3DPrimitive, ctx: PipelineContext
    ) -> None:
        """Raise CodegenError if a 3D primitive appears in an invalid pipe context."""
        # 3D primitives in pipes are only allowed in VertexSelection or PointSelection
        allowed = (
            PipelineContext.VERTEX_SELECTION,
            PipelineContext.POINT_SELECTION,
        )
        if ctx not in allowed:
            prim_name = OCPCodegen._3D_PRIMITIVE_NAMES.get(
                type(op.primitive), type(op.primitive).__name__.lower()
            )
            raise CodegenError(
                f"3D primitive '{prim_name}' in pipe requires vertex or point selection. "
                f"Use '| verts |' or '| points ... |' before '| {prim_name} ...'"
            )

    # --- Pipeline ---

    def _gen_pipeline(self, node: ast.Pipeline) -> str:
        code = self._gen_expr(node.source)
        ctx = self._source_context(node.source)
        prev_op = None
        for op in node.operations:
            # Validate 2D primitive context before generating code
            if isinstance(op, ast.Implicit2DPrimitive):
                self._validate_2d_primitive_context(op, ctx)
            if isinstance(op, ast.Implicit3DPrimitive):
                self._validate_3d_primitive_context(op, ctx)
            code = self._gen_pipe_op(code, op, prev_op=prev_op, ctx=ctx)
            ctx = self._op_output_context(op, ctx)
            prev_op = op
        return code

    # Dispatch table mapping AST node types to handler method names.
    # Handlers are called as method(current, op) by default.
    # Handlers listed in _PIPE_CTX_OPS also receive a ctx keyword argument.
    _PIPE_DISPATCH: dict[type, str] = {
        ast.FacesSelect:        "_gen_faces_select",
        ast.EdgesSelect:        "_gen_edges_select",
        ast.VerticesSelect:     "_gen_vertices_select",
        ast.PointsSelect:       "_gen_points_select",
        ast.Workplane:          "_gen_workplane",
        ast.AsTag:              "_gen_as_tag",
        ast.Fillet:             "_gen_pipe_fillet",
        ast.Chamfer:            "_gen_pipe_chamfer",
        ast.Shell:              "_gen_shell",
        ast.Offset:             "_gen_offset",
        ast.Diff:               "_gen_diff",
        ast.Union:              "_gen_union",
        ast.Inter:              "_gen_inter",
        ast.Place:              "_gen_place",
        ast.Hole:               "_gen_hole",
        ast.Cut:                "_gen_cut",
        ast.Extrude:            "_gen_extrude",
        ast.Revolve:            "_gen_revolve",
        ast.Sweep:              "_gen_sweep",
        ast.Loft:               "_gen_loft",
        ast.ColorOp:            "_gen_color",
        ast.Translate:          "_gen_translate",
        ast.Rotate:             "_gen_rotate",
        ast.Scale:              "_gen_scale",
        ast.Mirror:             "_gen_mirror",
        ast.Move:               "_gen_move",
        ast.MoveTo:             "_gen_moveto",
        ast.Implicit2DPrimitive: "_gen_implicit_2d",
        ast.Implicit3DPrimitive: "_gen_implicit_3d",
        ast.Grid:               "_gen_pipe_grid",
        ast.Polar:              "_gen_pipe_polar_op",
    }

    # Subset of _PIPE_DISPATCH whose handlers accept an extra ctx= keyword.
    _PIPE_CTX_OPS: frozenset[type] = frozenset({
        ast.Place, ast.Hole, ast.Translate, ast.Grid, ast.Polar,
    })

    def _gen_pipe_op(self, current: str, op: ast.Node, prev_op: ast.Node | None = None, ctx: PipelineContext = PipelineContext.UNKNOWN) -> str:
        # Insert implicit workplane when going from Face selection to 2D primitive
        if isinstance(op, ast.Implicit2DPrimitive) and isinstance(prev_op, ast.FacesSelect):
            current = f"{current}.workplane()"

        handler_name = self._PIPE_DISPATCH.get(type(op))
        if handler_name is None:
            raise CodegenError(f"Unknown pipe operation: {type(op).__name__}")

        handler = getattr(self, handler_name)
        if type(op) in self._PIPE_CTX_OPS:
            return handler(current, op, ctx=ctx)
        return handler(current, op)

    def _gen_pipe_fillet(self, current: str, op: ast.Fillet) -> str:
        return f"{current}.fillet({self._gen_expr(op.radius)})"

    def _gen_pipe_chamfer(self, current: str, op: ast.Chamfer) -> str:
        return f"{current}.chamfer({self._gen_expr(op.radius)})"

    def _gen_pipe_grid(self, current: str, op: ast.Grid, ctx: PipelineContext = PipelineContext.UNKNOWN) -> str:
        if ctx in (PipelineContext.FACE_SELECTION, PipelineContext.POINT_SELECTION):
            return self._gen_grid_on_face(current, op, ctx)
        return self._gen_grid_placement(current, op)

    def _gen_pipe_polar_op(self, current: str, op: ast.Polar, ctx: PipelineContext = PipelineContext.UNKNOWN) -> str:
        if ctx in (PipelineContext.FACE_SELECTION, PipelineContext.POINT_SELECTION):
            return self._gen_polar_on_face(current, op, ctx)
        return self._gen_polar_placement(current, op)

    # --- Center kwarg helper ---

    def _gen_centered_arg(self, center_node: ast.Node | None, ndim: int = 3) -> str:
        """Generate the centered argument string for primitives.

        Returns empty string if center is None (default centered=True).
        For BoolConst, returns the appropriate tuple.
        For TupleLit, generates per-axis values.
        """
        if center_node is None:
            return ""
        if isinstance(center_node, ast.BoolConst):
            if ndim == 3:
                val = "True" if center_node.value else "False"
                return f", centered=({val}, {val}, {val})"
            else:
                val = "True" if center_node.value else "False"
                return f", centered=({val}, {val})"
        if isinstance(center_node, ast.TupleLit):
            vals = [self._gen_expr(v) for v in center_node.values]
            return f", centered=({', '.join(vals)})"
        # Expression (e.g. variable): evaluate at runtime
        return f", centered={self._gen_expr(center_node)}"

    # --- 3D Primitives ---

    def _gen_box(self, node: ast.Box) -> str:
        w = self._gen_expr(node.width)
        h = self._gen_expr(node.height)
        d = self._gen_expr(node.depth)
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").box({w}, {h}, {d}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_cylinder(self, node: ast.Cylinder) -> str:
        h = self._gen_expr(node.height)
        r = self._gen_expr(node.radius)
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").cylinder({r}, {h}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_sphere(self, node: ast.Sphere) -> str:
        r = self._gen_expr(node.radius)
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").sphere({r}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_cone(self, node: ast.Cone) -> str:
        h = self._gen_expr(node.height)
        r1 = self._gen_expr(node.r1)
        r2 = self._gen_expr(node.r2)
        extras = ""
        if node.pnt:
            extras += f", pnt={self._gen_expr(node.pnt)}"
        if node.dir:
            extras += f", dir={self._gen_expr(node.dir)}"
        if node.angle:
            extras += f", angle={self._gen_expr(node.angle)}"
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").cone({r1}, {r2}, {h}{extras}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_torus(self, node: ast.Torus) -> str:
        r1 = self._gen_expr(node.r1)
        r2 = self._gen_expr(node.r2)
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").torus({r1}, {r2}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_wedge(self, node: ast.Wedge) -> str:
        dx = self._gen_expr(node.dx)
        dy = self._gen_expr(node.dy)
        dz = self._gen_expr(node.dz)
        ltx = self._gen_expr(node.ltx)
        centered = self._gen_centered_arg(node.center, ndim=3)
        code = f'cq.Workplane("XY").wedge({dx}, {dy}, {dz}, {ltx}{centered})'
        return self._gen_at_kwarg(code, node.at)

    # --- 2D Primitives ---

    def _gen_rect(self, node: ast.Rect) -> str:
        w = self._gen_expr(node.width)
        h = self._gen_expr(node.height)
        centered = self._gen_centered_arg(node.center, ndim=2)
        code = f'cq.Workplane("XY").rect({w}, {h}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_circle(self, node: ast.Circle) -> str:
        r = self._gen_expr(node.radius)
        centered = self._gen_centered_arg(node.center, ndim=2)
        code = f'cq.Workplane("XY").circle({r}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_ellipse(self, node: ast.Ellipse) -> str:
        rx = self._gen_expr(node.rx)
        ry = self._gen_expr(node.ry)
        centered = self._gen_centered_arg(node.center, ndim=2)
        code = f'cq.Workplane("XY").ellipse({rx}, {ry}{centered})'
        return self._gen_at_kwarg(code, node.at)

    def _gen_polyline(self, node: ast.Polyline) -> str:
        pts = self._gen_expr(node.points)
        return f'cq.Workplane("XY").polyline({pts}).close()'

    def _gen_polygon(self, node: ast.Polygon) -> str:
        n = self._gen_expr(node.n)
        r = self._gen_expr(node.r)
        return f'cq.Workplane("XY").polygon({n}, {r})'

    def _gen_text(self, node: ast.Text) -> str:
        content = self._gen_expr(node.content)
        size = self._gen_expr(node.size) if node.size else "10"
        return f'cq.Workplane("XY").text({content}, {size}, 1)'

    # --- Sketch ---

    def _gen_sketch(self, node: ast.SketchExpr) -> str:
        return self._gen_sketch_segments('cq.Workplane("XY")', node)

    def _gen_sketch_segments(self, current: str, node: ast.SketchExpr) -> str:
        """Generate sketch(...) call with segments on the given workplane expression."""
        start = self._gen_expr(node.start)
        parts = [f'{current}.sketch({start}']
        for seg in node.segments:
            if isinstance(seg, ast.TupleLit):
                pt = self._gen_expr(seg)
                parts.append(f', ("line", {pt})')
            elif isinstance(seg, ast.ArcPath):
                through = self._gen_expr(seg.through)
                end = self._gen_expr(seg.end)
                parts.append(f', ("arc", {through}, {end})')
            elif isinstance(seg, ast.BezierPath):
                pts = self._gen_expr(seg.points)
                parts.append(f', ("bezier", {pts})')
        parts.append(")")
        return "".join(parts)

    # --- Path Primitives ---

    def _gen_line_path(self, node: ast.LinePath) -> str:
        s = self._gen_expr(node.start)
        e = self._gen_expr(node.end)
        return f'cq.Workplane("XY").moveTo({s}[0], {s}[1]).lineTo({e}[0], {e}[1])'

    def _gen_arc_path(self, node: ast.ArcPath) -> str:
        # Simplified arc generation
        t = self._gen_expr(node.through)
        e = self._gen_expr(node.end)
        return f'.threePointArc({t}, {e})'

    def _gen_helix_path(self, node: ast.HelixPath) -> str:
        pitch = self._gen_expr(node.pitch)
        h = self._gen_expr(node.height)
        r = self._gen_expr(node.radius)
        return f'cq.Wire.makeHelix(pitch={pitch}, height={h}, radius={r})'

    def _gen_bezier_path(self, node: ast.BezierPath) -> str:
        pts = self._gen_expr(node.points)
        return f'cq.Workplane("XY").spline({pts})'

    def _gen_spline_path(self, node: ast.SplinePath) -> str:
        pts = self._gen_expr(node.points)
        return f'cq.Workplane("XY").spline({pts})'

    # --- Pipe Operations ---

    def _gen_selector_arg(self, op) -> str | None:
        """Generate a selector string.
        Multiple selectors → AND (join with ' and ').
        Single list selector → OR (join with ' or ')."""
        if op.selectors:
            parts = [self._gen_expr(s) for s in op.selectors]
            return ' + " and " + '.join(parts)
        if op.selector is not None:
            # List literal → OR: edges [>Z, >X]
            if isinstance(op.selector, ast.ListLit):
                parts = [self._gen_expr(v) for v in op.selector.values]
                return ' + " or " + '.join(parts)
            return self._gen_expr(op.selector)
        return None

